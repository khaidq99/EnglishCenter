package model;

import java.io.Serializable;

/**
 *
 * @author KHAI-PC
 */
public class Room implements Serializable {
    private int id;
    private String name;
    private int capacity;
    private String des;

    public Room() {
    }

    public Room(int id, String name, int capacity, String des) {
        this.id = id;
        this.name = name;
        this.capacity = capacity;
        this.des = des;
    }

    public Room(String name, int capacity, String des) {
        this.name = name;
        this.capacity = capacity;
        this.des = des;
    }
    
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getCapacity() {
        return capacity;
    }

    public void setCapacity(int capacity) {
        this.capacity = capacity;
    }

    public String getDes() {
        return des;
    }

    public void setDes(String des) {
        this.des = des;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Room other = (Room) obj;
        if (this.id != other.id) {
            return false;
        }
        return true;
    }
}
